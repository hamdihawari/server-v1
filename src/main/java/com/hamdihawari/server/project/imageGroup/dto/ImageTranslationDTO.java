package com.hamdihawari.server.project.imageGroup.dto;

public class ImageTranslationDTO {
    private Long id;
    private String languageCode; // Code for the language
    private String data; // Main data for the image translation
    private String imageSubject; // Subject of the image
    private String imageDescription; // Description of the image

    // Default constructor
    public ImageTranslationDTO() {}

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getImageSubject() {
        return imageSubject;
    }

    public void setImageSubject(String imageSubject) {
        this.imageSubject = imageSubject;
    }

    public String getImageDescription() {
        return imageDescription;
    }

    public void setImageDescription(String imageDescription) {
        this.imageDescription = imageDescription;
    }

    public String getLanguageCode() {
        return languageCode;
    }

    public void setLanguageCode(String languageCode) {
        this.languageCode = languageCode;
    }
}


